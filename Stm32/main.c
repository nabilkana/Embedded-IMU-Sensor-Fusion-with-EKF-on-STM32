/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_USART2_UART_Init();
    MX_I2C1_Init();

    while( !MPU9250_Init(&hi2c1, &huart2)) HAL_Delay(3000);

    AccelData acc;
    GyroData gyr;

    char msg_local[128];
    float filtered_ax = 0, filtered_ay = 0, filtered_az = 0;
    float filtered_gx = 0, filtered_gy = 0, filtered_gz = 0;
    float pitch =0 , roll = 0 ;
    float pitch_gyro = 0.0f;
    float roll_gyro = 0.0f;
    float pitch_acc, roll_acc;
    float timestamp, dt;
    float alpha = 0.15f , alphac =0.9f;
    float denom;float denom_roll ;
    EKF_t ekf;
    ekf_init(&ekf);
    uint32_t last_time = HAL_GetTick();
    float gyro[INPUT_DIM];
    static float accel_angles_filt[2] = {0};
   alpha = 0.95f;
    float accel_angles[MEAS_DIM];
    while (1)
    {
    	uint8_t acc_ok = MPU9250_ReadAccel(&hi2c1, &acc);
    	uint8_t gyr_ok = MPU9250_ReadGyro(&hi2c1, &gyr);

    	if (!acc_ok || !gyr_ok) {
    	    uart_print(&huart2, "MPU9250 read failed\r\n");
    	    continue;
    	}


        uint32_t now = HAL_GetTick();
        dt = (now - last_time) / 1000.0f;
        last_time = now;
        gyro[0] = gyr.gx*(3.14159f / 180.0f);
                        gyro[1] = gyr.gy*(3.14159f / 180.0f);
                        gyro[2] = gyr.gz*(3.14159f / 180.0f);


                        accel_angles[0] = atan2f(acc.ay, acc.az);
                        denom = sqrtf(acc.ay * acc.ay + acc.az * acc.az);
                        accel_angles[1] = atan2f(-acc.ax, denom);
                        // EKF predict step
                        ekf_predict(&ekf, gyro, dt);
                        accel_angles_filt[0] = alpha * accel_angles_filt[0] + (1 - alpha) * accel_angles[0];
                        // EKF update step
                        ekf_update(&ekf, accel_angles_filt,&huart2);


                        float roll_deg = ekf.x[0] * (180.0f / 3.14159265f);
                        float pitch_deg = ekf.x[1] * (180.0f / 3.14159265f);


        timestamp = now / 1000.0f;
                 HAL_Delay(20);

                sprintf(msg_local, "%.2f,%.3f,%.3f\r\n",timestamp,roll_deg,  pitch_deg );
                uart_print(&huart2, msg_local);

    }
}


